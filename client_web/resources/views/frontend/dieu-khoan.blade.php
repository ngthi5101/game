                ĐIỀU KHOẢN SỬ DỤNG Vin88<br>
                <br>
                <br>
                Chào mừng bạn đến với Vin88 . Để đăng ký tài khoản và sử dụng dịch vụ, vui lòng đọc kỹ các thỏa thuận sử dụng dịch vụ của chúng tôi dưới đây:<br>
                <br>
                1. Các thỏa thuận chung<br>
                <br>
                1.1. Bạn không được phép buôn bán đơn vị tiền ảo trong dịch vụ để thu lợi và hay chuyển đổi sang tiền VNĐ.<br>
                <br>
                1.2. Bạn không được phép cung cấp các website, email, hoặc bất cứ hình thức lừa đảo nào nhằm lấy thông tin tài khoản hay tiền ảo trong dịch vụ của các tài khoản khác.<br>
                <br>
                1.3. Bạn không được phép nhận tiền ảo trong dịch vụ từ website, email, tài khoản lừa đảo hay các nguồn bất chính<br>
                <br>
                1.4. Nếu chúng tôi phát hiện bạn vi phạm bất kỳ quy định nào trong thỏa thuận sử dụng này thì ngay lập tức khóa tài khoản mà không cần báo trước.<br>
                <br>
                1.5. Trong quá trình giao dịch nếu có lỗi xảy ra, bạn vui lòng liên hệ&nbsp; để được trợ giúp.<br>
                <br>
                1.6. Bạn hiểu và chấp nhận rằng khi xảy ra tranh chấp, căn cứ để giải quyết tranh chấp giữa bạn và hệ thống dịch vụ là bản thỏa thuận này.<br>
                <br>
                1.7. Nghiêm cấm dò thông tin bảo mật để chiếm đoạt tài khoản của người khác<br>
                <br>
                2. Quy định về tài khoản và thông tin chủ tài khoản<br>
                Để sử dụng các dịch vụ, người sử dụng cần phải đăng ký tài khoản và các thông tin về chủ sở hữu tài khoản.<br>
                <br>
                2.1. Quy định tên tài khoản:<br>
                - Tên tài khoản gồm các ký tự bảng chữ cái tiếng Anh, chữ số, dấu gạch dưới.<br>
                - Khi tạo lập tài khoản điền đầy đủ các thông tin được yêu cầu khi thực hiện đăng ký.<br>
                - Việc tạo lập tài khoản cần tuân thủ các nguyên tắc sau:<br>
                + Không được đặt tên và tạo biểu tượng liên quan đến các danh nhân, tên của các vị lãnh đạo Đảng và Nhà Nước.&lt;<br>
                + Không được đặt tên và tạo biểu tượng có nội dung phản động, bài xích tôn giáo, khiêu dâm, bạo lực, đi ngược lại thuần phong mỹ tục, truyền thống và văn hóa Việt Nam.<br>
                + Không được đặt tên và tạo biểu tượng có nội dung xúc phạm, khích bác đến người khác dưới bất kỳ hình thức nào.<br>
                + Không được đặt tên và tạo biểu tượng tên kẻ xấu (trùm khủng bố, phát xít…)<br>
                <br>
                2.2. Thông tin chủ tài khoản.<br>
                - Chủ tài khoản phải tự bảo quản tài khoản, mật khẩu, email và ghi các thông tin cần thiết phòng khi có sự cố xảy ra (ví dụ: bị hack.v.v…). Chúng tôi không chịu trách nhiệm trước bất kỳ tổn thất nào của bạn do quá trình sử dụng dịch vụ gây ra.<br>
                - Email của tài khoản là email được sử dụng duy nhất trong hệ thống. Chủ tài khoản tự chịu trách nhiệm về bảo mật email và sử dụng email để đăng ký trong hệ thống.<br>
                <br>
                2.3. Quy định về việc thanh lọc tài khoản<br>
                - Thời gian thanh lọc định kỳ: Thanh lọc thường xuyên đối với tài khoản hoàn toàn không có hoạt động phát sinh giao dịch trên hệ thống dịch vụ<br>
                - Xóa bỏ khỏi hệ thống: Với những tài khoản trong thời gian 6 tháng không phát sinh bất kỳ giao dịch nào trên hệ thống dịch vụ, hệ thống sẽ tiến hành thanh lọc và xóa những tài khoản đó.<br>
                <br>
                3. Quy định về đăng tải thông tin<br>
                Chúng tôi có quyền bổ sung, sửa đổi hay xóa bỏ bất kỳ thông tin, quy định nào cũng như thay đổi việc trình bày, thành phần hoặc chức năng ... của hệ thống mà không cần báo trước.<br>
                <br>
                4. Yêu cầu đối với chủ tài khoản<br>
                Chủ tài khoản phải tuân thủ các quy định trong thỏa thuận này cũng như các hành vi dưới đây:<br>
                <br>
                4.1. Không được xâm phạm an ninh hoặc có hành vi tìm cách xâm phạm an ninh hệ thống máy chủ của của website.<br>
                <br>
                4.2. Không được sử dụng các công cụ làm thay đổi kết quả chơi hoặc có các hành vi gian lận để phá hoại hoặc trục lợi.<br>
                <br>
                4.3. Không được mạo nhận là các thành viên khác (người quản trị hệ thống, chuyên viên hỗ trợ,... của hệ thống dịch vụ).<br>
                <br>
                4.4. Không phát tán và truyền bá thông tin bất hợp pháp, lừa gạt, bôi nhọ, sỉ nhục, tục tĩu, khiêu dâm, xúc phạm, đe dọa, lăng mạ, thù hận, kích động… hoặc trái với chuẩn mực đạo đức chung của xã hội.<br>
                <br>
                4.5. Không gửi thông tin quảng cáo, thư dây chuyền hay bất kỳ dạng liên lạc có mục đích thương mại.<br>
                <br>
                4.6. Không được gửi hoặc truyền bất kỳ thông tin hoặc phần mềm nào có chứa bất kỳ loại virus, trojan, bọ hay các thành phần nguy hại nào đến sự an toàn của hệ thống dịch vụ.<br>
                <br>
                4.8. Không được gửi liên tục các dòng chữ vô nghĩa trên các kênh chat nhằm cản trở hoặc quấy rối các thành viên khác.<br>
                <br>
                4.9. Không được có thái độ đe dọa hoặc các hành vi quấy rối, gây ức chế tâm lý,... đối với các thành viên khác.<br>
                <br>
                4.10. Không được sử dụng những từ ngữ, lời lẽ, dấu hiệu, ký tự không lành mạnh, tục tĩu, phỉ báng, xúc phạm,... nhân viên quản trị hệ thống dịch vụ, người chơi và các tổ chức khác.<br>
                <br>
                4.11. Không được có hành vi câu kết, gây rối, cản trở, phá hoại, bịa đặt thông tin gây hại cho cộng đồng, nhà phát hành hoặc đối tác liên kết với nhà phát hành trong mọi chương trình, sự kiện, dịch vụ. Nhà phát hành hoặc ban tổ chức sẽ có những biện pháp xử lý như khóa tài khoản hoặc tước mọi quyền lợi của chủ tài khoản vi phạm<br>
                <br>
                4.12. Cung cấp đầy đủ, chính xác các thông tin cá nhân như họ tên, giới tính, ngày sinh, email, địa chỉ, số chứng minh thư nhân dân cũng như những yếu tố khác có ý nghĩa trong việc xác định quyền sở hữu đối với tài khoản theo yêu cầu của doanh nghiệp cung cấp dịch vụ trò chơi trực tuyến. Nhà phát hành có quyền hủy quyền lợi của tài khoản trong trường hợp thông tin cá nhân không đầy đủ hoặc không chính xác trong thời gian xét duyệt tài khoản được hưởng quyền lợi.<br>
                <br>
                4.13. Sử dụng đầy đủ các công cụ bảo mật cho tài khoản: bảo mật đăng nhập ODP, OTP, bảo mật số dư bằng két bảo mật để bảo vệ tài khoản và số dư. Trong trường hợp tài khoản bị hack và tài khoản hack không có khả năng hoàn trả số dư, tài khoản không chứng minh được đã sử dụng đầy đủ các công cụ bảo mật nói trên sẽ không được hỗ trợ truy thu, hoàn trả số dư.<br>
                <br>
                5. Quyền hạn của Nhà phát hành<br>
                <br>
                5.1. Nếu phát hiện người sử dụng vi phạm các quy định đã nêu trong Bản thoả thuận này, chúng tôi có quyền cấm hoặc từ chối truy cập của bất cứ tài khoản nào mà không cần báo trước. Trong những trường hợp nghiêm trọng, Nhà phát hành sẽ phối hợp với chính quyền để truy cứu trách nhiệm đối với những người vi phạm.<br>
                <br>
                5.2. Chúng tôi có quyền sử dụng thông tin tài khoản để gửi thông báo cho chủ tài khoản về các sản phẩm và dịch vụ hoặc chương trình khuyến mại thông qua thư điện tử hoặc thư bưu chính. Chúng tôi cũng có thể sử dụng các thông tin này để tiến hành các cuộc điều tra (Ví dụ: thông báo thay đổi dịch vụ trong hệ thống, thông báo về các chương trình khuyến mại hay các hành động nhân đạo và xã hội khác). Chúng tôi duy trì chính sách "KHÔNG GỬI THƯ RÁC", không chia sẻ, bán hay để lọt email của chủ tài khoản cho các bên thứ ba.<br>
                <br>
                5.3. Chúng tôi có toàn quyền thay đổi những điều khoản, chính sách mà chúng tôi cho rằng không còn phù hợp trong thoả thuận này mà không cần báo trước, người sử dụng mặc nhiên đồng ý chấp hành các sửa đổi trong quy định mới.<br>
                <br>
                5.4. Đối với các tài khoản vi phạm điều khoản trên, chúng tôi có quyền khóa tài khoản hay hủy mọi quyền lợi tùy theo mức độ vi phạm<br>
                <br>
                5.5. Chúng tôi sẽ chỉ cung cấp thông tin cá nhân và/hoặc địa chỉ IP của chủ tài khoản khi được các cơ quan luật pháp Việt Nam yêu cầu và để làm các việc thực sự cần thiết như sau:<br>
                - Phối hợp với cơ quan điều tra để tìm ra những hoạt động bất hợp pháp liên quan đến việc phát tán thông tin và an ninh mạng.<br>
                - Bảo vệ quyền và tài sản liên quan đến hệ thống dịch vụ do chúng tôi cung cấp.<br>
                - Nhận dạng những người cố tình vi phạm luật thông tin và an ninh mạng.<br>
                <br>
                5.6. Trong các sự kiện được tổ chức, chúng tôi có quyền hủy giải thưởng khách hàng nếu thông tin tài khoản khách hàng không phải là chính chủ khách hàng đó, hoặc phát hiện hành vi buôn bán trái phép giữa các tài khoản.<br>
                <br>
                5.7. Giải quyết khiếu nại, hack: chúng tôi có quyền từ chối giải quyết khiếu nại, bị hack tài khoản, số dư nếu tài khoản hack không còn số dư và tài khoản bị hack không đảm bảo được việc sử dụng đầy đủ các công cụ bảo mật (ODP, OTP và két bảo mật) trước và trong thời điểm bị hack.<br>
                <br>
                5.8 : Chúng tôi từ chối giải quyết các trường hợp khiếu nại mà thời điểm xảy ra quá 2 tuần so với thời điểm nhận khiếu nại.<br>
                <br>
                5.9 : Nhà phát hành có quyền khóa các tài khoản nằm trong diện nghi vấn hack hoặc sử dụng các biện pháp gian lận trong game.<br>
                <br>
                6. Khiếu nại<br>
                <br>
                6.1. Khi tố cáo, khiếu nại một tài khoản khác bạn cần nêu rõ tên tài khoản bị tố cáo, khiếu nại và hành vi vi phạm của tài khoản đó. Để được bảo vệ quyền lợi, bạn vui lòng cung cấp đủ ba thông tin về tài khoản của mình (bao gồm: CMND, câu hỏi và câu trả lời bảo mật, địa chỉ email khi đăng ký tài khoản) và địa chỉ liên hệ cần thiết.<br>
                <br>
                6.2. Trong quá trình tố cáo, khiếu nại, bạn có thể yêu cầu nhân viên tiếp nhận khóa tài khoản của mình để bảo vệ tài sản, bảo toàn số dư còn lại trong tài khoản.<br>
                <br>
                6.3. Trong trường hợp tố cáo, khiếu nại hack không đúng sự thật, tài khoản của bạn sẽ bị phạt khóa trong thời gian 02 tuần.<br>
                <br>
                6.4. Trong trường hợp xảy ra tranh chấp bắt buộc phải đối chất, chúng tôi có trách nhiệm thông báo thời gian cụ thể cho bạn. Bạn hoặc người ủy quyền của bạn phải có mặt tại địa điểm Nhà phát hành đưa ra.<br>
                <br>
                7. Điều khoản thi hành.<br>
                <br>
                Thoả thuận sử dụng này ràng buộc chúng tôi kể từ khi được đưa lên website dịch vụ và ràng buộc Quý khách hàng kể từ khi đăng ký sử dụng dịch vụ. Khi có sửa đổi bổ sung được đưa lên thì chúng tôi và Quý khách hàng đều phải tuân thủ những sửa đổi bổ sung do chúng tôi đưa ra.
